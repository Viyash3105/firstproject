package com.cg.daytwo;

public class if_else_ladder {
	public static void main(String [] args) {
		int x=20;
		
		if(x == 10) {
			System.out.println("value of x is :"+x);
		}else if(x == 20) {
			System.out.println("value of x is :"+x);
		}else if(x == 30) {
			System.out.println("value of x is :"+x);
		}else  {
			System.out.println("value of x is :"+x);
		}
	}

}
